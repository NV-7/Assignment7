public class Points {

    private int edge;
    private String name;

    public Points(){
        edge = 0;
        name = null;
    }

    public void setEdge(int edge) {
        this.edge = edge;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public int getEdge() {
        return edge;
    }
}
